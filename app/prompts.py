from typing import List, Dict, Any


def format_context(retrieved_chunks: List[Dict[str, Any]]) -> str:
    """
    Превращает найденные chunks в текстовый контекст для LLM.
    """
    context_parts = []

    for i, chunk in enumerate(retrieved_chunks, start=1):
        context_parts.append(
            f"""
[Фрагмент {i}]
Источник: {chunk["source"]}
Раздел: {chunk["title"]}

{chunk["text"]}
""".strip()
        )

    return "\n\n---\n\n".join(context_parts)


def build_rag_prompt(
    question: str,
    retrieved_chunks: List[Dict[str, Any]],
    coordinator_settings: Dict[str, str],
) -> str:
    """
    Собирает prompt для RAG-ответа.
    """
    context = format_context(retrieved_chunks)

    prompt = f"""
Ты — вежливый и аккуратный помощник по конкурсу «Большие вызовы» в Новосибирской области.

Твоя задача — отвечать на вопросы школьников, родителей и координаторов простым понятным языком.

Используй только:
1. найденный контекст из базы знаний;
2. настройки координатора.

Не придумывай даты, правила, контакты, направления и требования.
Если в контексте нет точного ответа, честно скажи, что информации недостаточно, и посоветуй проверить официальный сайт или уточнить у координатора.

Важные правила:
- отвечай спокойно, дружелюбно и понятно;
- не обещай победу или прохождение отбора;
- не проси ФИО, телефон, адрес, школу и другие персональные данные;
- не помогай писать проект за участника;
- различай основной конкурс «Большие вызовы» и региональный конкурс «ПРО Большие вызовы»;
- если вопрос касается возраста, класса, сроков или регистрации, отвечай особенно аккуратно.

Настройки координатора:
- Регион: {coordinator_settings.get("region", "не указан")}
- Текущий этап: {coordinator_settings.get("current_stage", "не указан")}
- Дедлайн подачи заявок: {coordinator_settings.get("deadline", "не указан")}
- Сообщение координатора: {coordinator_settings.get("coordinator_note", "не указано")}

Контекст из базы знаний:
{context}

Вопрос пользователя:
{question}

Ответь на русском языке. Сначала дай прямой ответ, затем при необходимости добавь уточнение.
Не перечисляй технические scores и chunk_id.
""".strip()

    return prompt